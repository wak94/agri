class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        # ç¬¬iè¡Œj+1å‡ºçŽ°çš„æ¬¡æ•°
        row = [[0] * 9 for _ in range(9)]
        col = [[0] * 9 for _ in range(9)]
        square = [[[0] * 9 for _ in range(3)] for _ in range(3)]
        for i in range(9):
            for j in range(9):
                c = board[i][j]
                if c != '.':
                    x = int(c) - 1
                    row[i][x] += 1
                    col[j][x] += 1
                    square[i // 3][j // 3][x] += 1
                    if row[i][x] > 1 or col[j][x] > 1 or square[i // 3][j // 3][x] > 1:
                        return False
        return True
